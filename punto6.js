// Solicitar la longitud del lado
let lado = parseFloat(prompt("Ingrese la longitud del lado del cuadrado:"));

// Calcular el perímetro (P = 4 × lado)
let perimetro = 4 * lado;

// Mostrar resultado
alert("El perímetro del cuadrado es: " + perimetro);
